print('Listing Example: ******************')
ls = [1, "Apple in List", "Banana in List"]
for i in ls:
    print(i)

print('Tuple Example: ********************')
tpls = (1, "Apple in Tuple", "Banana in Tuples")
for i in tpls:
    print(i)

print('Set Example: ********************')
sets = {1, "Apple in Set", "Banana in Set"}
for i in sets:
    print(i)

print('Dictionary Example: ********************')
dics = {
    "value1": "Apple in Dictionary",
    "value2": "Banana in Dictionary",
    "value3": 8
}
for i in dics:
    print(dics[i])
